def check_docker_status(service_name: str) -> str:
    """Check if a Docker container is running and healthy."""
    import subprocess
    result = subprocess.run(
        ["docker", "inspect", "--format",
         "{{.State.Status}} | Health: {{.State.Health.Status}}", service_name],
        capture_output=True, text=True, timeout=10
    )
    return result.stdout.strip() or f"Service {service_name} not found"


def get_system_metrics() -> dict:
    """Get current system CPU, memory, and disk metrics."""
    import psutil
    return {
        "cpu_percent": psutil.cpu_percent(interval=1),
        "memory_percent": psutil.virtual_memory().percent,
        "disk_percent": psutil.disk_usage('/').percent,
        "timestamp": datetime.utcnow().isoformat(),
    }


def generate_ops_report(findings: str, recommendations: str) -> str:
    """Generate a formatted ops report."""
    timestamp = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")
    return f"""
# Ops Report — {timestamp}

## Findings
{findings}

## Recommendations
{recommendations}

---
*Generated by AgentScope Ops Pipeline*
"""


