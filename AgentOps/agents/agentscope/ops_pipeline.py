# ─────────────────────────────────────────────
# Pipeline: Sequential Ops Workflow
# ─────────────────────────────────────────────
def build_ops_pipeline():
    """Build a sequential ops pipeline: Monitor → Analyse → Remediate → Report."""
    
    pipeline = SequentialPipeline(
        agents=[monitor_agent, analyst_agent, remediation_agent, report_agent]
    )
    return pipeline


# ─────────────────────────────────────────────
# Pipeline: Conditional Escalation
# ─────────────────────────────────────────────
def build_escalation_pipeline():
    """Build an if-else pipeline that escalates critical issues."""
    
    def is_critical(msg: Msg) -> bool:
        content = str(msg.content).lower()
        return "critical" in content or "p0" in content or "down" in content

    escalation_agent = DialogAgent(
        name="EscalationAgent",
        model_config_name=ACTIVE_MODEL,
        sys_prompt=(
            "You handle critical escalations. Notify the on-call engineer "
            "and generate an immediate incident ticket. Draft the PagerDuty alert message."
        ),
        use_memory=False,
    )

    pipeline = IfElsePipeline(
        condition_func=is_critical,
        if_body_agent=escalation_agent,
        else_body_agent=report_agent,
    )
    return pipeline


# ─────────────────────────────────────────────
# Pipeline: Retry Loop
# ─────────────────────────────────────────────
def build_retry_pipeline(max_retries: int = 3):
    """Build a while-loop pipeline that retries health checks until stable."""
    
    retry_count = {"n": 0}

    def should_retry(msg: Msg) -> bool:
        retry_count["n"] += 1
        content = str(msg.content).lower()
        return "degraded" in content and retry_count["n"] < max_retries

    pipeline = WhileLoopPipeline(
        loop_body_agent=monitor_agent,
        condition_func=should_retry,
    )
    return pipeline


